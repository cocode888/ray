function F=SVequation2(a,zheta,zt)

zero=complex(0,0);one=complex(1,0);
deltz=eye(3,'like',complex(1,0));
aR=real(a);
Q=-aR./imag(a);
b=[aR(1);aR(2);aR(3);aR(4);Q(1);Q(2);Q(3);Q(4)];
n=[sin(zheta+1i*zt),0,cos(zheta+1i*zt)];

G11=a(1)*n(1)^2+a(4)*n(3)^2;
G22=a(5)*n(1)^2+a(4)*n(3)^2;
G33=a(4)*n(1)^2+a(3)*n(3)^2;
G13=(a(2)+a(4))*n(1)*n(3);
c(1)=sqrt(0.5*(G11+G33+sqrt((G11+G33)^2-4*(G11*G33-G13^2))));
c(2)=sqrt(0.5*(G11+G33-sqrt((G11+G33)^2-4*(G11*G33-G13^2))));
c(3)=sqrt(G22);

p1=n/c(2); 

G1(1,1)=a(1)*p1(1)*p1(1)+a(4)*p1(3)*p1(3);
G1(1,2)=complex(0,0);
G1(1,3)=(a(2)+a(4))*p1(1)*p1(3);
G1(2,1)=G1(1,2);
G1(2,2)=a(5)*p1(1)*p1(1)+a(4)*p1(3)*p1(3);
G1(2,3)=complex(0,0);
G1(3,1)=G1(1,3);
G1(3,2)=G1(2,3);
G1(3,3)=a(4)*p1(1)*p1(1)+a(3)*p1(3)*p1(3);
GM1=G1-deltz;
% ---------- 9 possiable rank(GM1)=2 --------------
B1(1)=GM1(1,1)*GM1(2,2)-GM1(1,2)*GM1(1,2);
B1(2)=GM1(1,1)*GM1(2,3)-GM1(1,3)*GM1(1,2);
B1(3)=GM1(1,2)*GM1(2,3)-GM1(1,3)*GM1(2,2);
B1(4)=GM1(1,1)*GM1(3,2)-GM1(1,2)*GM1(3,1);
B1(5)=GM1(1,1)*GM1(3,3)-GM1(1,3)*GM1(3,1);
B1(6)=GM1(1,2)*GM1(3,3)-GM1(1,3)*GM1(3,2);
B1(7)=GM1(2,1)*GM1(3,2)-GM1(2,2)*GM1(3,1);
B1(8)=GM1(2,1)*GM1(3,3)-GM1(2,3)*GM1(3,1);
B1(9)=GM1(2,2)*GM1(3,3)-GM1(2,3)*GM1(3,2);

vA13(1)=one; vA13(2)=-B1(8)/B1(9); vA13(3)= B1(7)/B1(9);
sqvA13=sqrt(vA13*vA13'); gA13=vA13/sqvA13;

LA31=real(gA13(1)*gA13(1)'); 
LA33=real(gA13(3)*gA13(3)'); 
LA35=real(gA13(1)*gA13(3)'+gA13(3)*gA13(1)')/2;

kpalaA31=LA31*LA35*b(1)*b(2)*(1/b(5)-1/b(6))+LA31*LA35*b(1)*b(4)*(1/b(5)-1/b(8))+LA33*LA35*b(2)*b(4)*(1/b(8)-1/b(6));
kpalaA33=LA31*LA31*b(1)*b(4)*(1/b(5)-1/b(8))+LA31*LA33*b(1)*b(3)*(1/b(5)-1/b(7))+LA33*LA33*b(3)*b(4)*(1/b(8)-1/b(7));
kpalaA32=LA31*LA35*b(2)*b(4)*(1/b(6)-1/b(8))+LA33*LA35*b(2)*b(3)*(1/b(6)-1/b(7))+LA33*LA35*b(3)*b(4)*(1/b(8)-1/b(7));
kpalaA34=LA35*LA35*b(2)*b(2)*(1+1/(b(6)*b(6)))+LA35*LA35*2*b(2)*b(4)*(1+1/(b(6)*b(8)))+...
    LA35*LA35*b(4)*b(4)*(1+1/(b(8)*b(8)))-LA31*LA31*b(1)*b(4)*(1+1/(b(5)*b(8)))-LA31*LA33*b(1)*b(3)*(1+1/(b(5)*b(7)))-...
    LA31*LA33*b(4)*b(4)*(1+1/(b(8)*b(8)))-LA33*LA33*b(3)*b(4)*(1+1/(b(7)*b(8)));


F=(kpalaA31+kpalaA32)*cosh(2*zt)+kpalaA34*sinh(2*zt)+(kpalaA32-kpalaA31)*cos(2*zheta)+(kpalaA33)*sin(2*zheta);

end