function F=SVequation1(a,zheta,zt)

zero=complex(0,0);one=complex(1,0);
deltz=eye(3,'like',complex(1,0));
aR=real(a);
Q=-aR./imag(a);
b=[aR(1);aR(2);aR(3);aR(4);Q(1);Q(2);Q(3);Q(4)];
n=[sin(zheta+1i*zt),0,cos(zheta+1i*zt)];

G11=a(1)*n(1)^2+a(4)*n(3)^2;
G22=a(5)*n(1)^2+a(4)*n(3)^2;
G33=a(4)*n(1)^2+a(3)*n(3)^2;
G13=(a(2)+a(4))*n(1)*n(3);
c(1)=sqrt(0.5*(G11+G33+sqrt((G11+G33)^2-4*(G11*G33-G13^2))));
c(2)=sqrt(0.5*(G11+G33-sqrt((G11+G33)^2-4*(G11*G33-G13^2))));
c(3)=sqrt(G22);

p1=n/c(2); 

G1(1,1)=a(1)*p1(1)*p1(1)+a(4)*p1(3)*p1(3);
G1(1,2)=complex(0,0);
G1(1,3)=(a(2)+a(4))*p1(1)*p1(3);
G1(2,1)=G1(1,2);
G1(2,2)=a(5)*p1(1)*p1(1)+a(4)*p1(3)*p1(3);
G1(2,3)=complex(0,0);
G1(3,1)=G1(1,3);
G1(3,2)=G1(2,3);
G1(3,3)=a(4)*p1(1)*p1(1)+a(3)*p1(3)*p1(3);
GM1=G1-deltz;
% ---------- 9 possiable rank(GM1)=2 --------------
B1(1)=GM1(1,1)*GM1(2,2)-GM1(1,2)*GM1(1,2);
B1(2)=GM1(1,1)*GM1(2,3)-GM1(1,3)*GM1(1,2);
B1(3)=GM1(1,2)*GM1(2,3)-GM1(1,3)*GM1(2,2);
B1(4)=GM1(1,1)*GM1(3,2)-GM1(1,2)*GM1(3,1);
B1(5)=GM1(1,1)*GM1(3,3)-GM1(1,3)*GM1(3,1);
B1(6)=GM1(1,2)*GM1(3,3)-GM1(1,3)*GM1(3,2);
B1(7)=GM1(2,1)*GM1(3,2)-GM1(2,2)*GM1(3,1);
B1(8)=GM1(2,1)*GM1(3,3)-GM1(2,3)*GM1(3,1);
B1(9)=GM1(2,2)*GM1(3,3)-GM1(2,3)*GM1(3,2);

vA11(1)=B1(3)/B1(1);  vA11(2)=-B1(2)/B1(1);  vA11(3)=one; 
sqvA11=sqrt(vA11*vA11'); gA11=vA11/sqvA11;

LA11=real(gA11(1)*gA11(1)'); 
LA13=real(gA11(3)*gA11(3)'); 
LA15=real(gA11(1)*gA11(3)'+gA11(3)*gA11(1)')/2;

kpalaA11=LA11*LA15*b(1)*b(2)*(1/b(5)-1/b(6))+LA11*LA15*b(1)*b(4)*(1/b(5)-1/b(8))+LA13*LA15*b(2)*b(4)*(1/b(8)-1/b(6));
kpalaA13=LA11*LA11*b(1)*b(4)*(1/b(5)-1/b(8))+LA11*LA13*b(1)*b(3)*(1/b(5)-1/b(7))+LA13*LA13*b(3)*b(4)*(1/b(8)-1/b(7));
kpalaA12=LA11*LA15*b(2)*b(4)*(1/b(6)-1/b(8))+LA13*LA15*b(2)*b(3)*(1/b(6)-1/b(7))+LA13*LA15*b(3)*b(4)*(1/b(8)-1/b(7));
kpalaA14=LA15*LA15*b(2)*b(2)*(1+1/(b(6)*b(6)))+LA15*LA15*2*b(2)*b(4)*(1+1/(b(6)*b(8)))+...
    LA15*LA15*b(4)*b(4)*(1+1/(b(8)*b(8)))-LA11*LA11*b(1)*b(4)*(1+1/(b(5)*b(8)))-LA11*LA13*b(1)*b(3)*(1+1/(b(5)*b(7)))-...
    LA11*LA13*b(4)*b(4)*(1+1/(b(8)*b(8)))-LA13*LA13*b(3)*b(4)*(1+1/(b(7)*b(8)));

F=(kpalaA11+kpalaA12)*cosh(2*zt)+kpalaA14*sinh(2*zt)+(kpalaA12-kpalaA11)*cos(2*zheta)+(kpalaA13)*sin(2*zheta);

end