function G=Christoffel_matrix_VTI(a,v1,v2)
G(1,2)=(a(1)-a(5))*(v1(1)*v2(2)+v1(2)*v2(1))/2;
G(1,3)=(a(2)+a(4))*(v1(1)*v2(3)+v1(3)*v2(1))/2;
G(2,3)=(a(1)+a(4))*(v1(2)*v2(3)+v1(3)*v2(2))/2;
G(1,1)=a(1)*v1(1)*v2(1)+a(5)*v1(2)*v2(2)+a(4)*v1(3)*v2(3);
G(2,2)=a(5)*v1(1)*v2(1)+a(1)*v1(2)*v2(2)+a(4)*v1(3)*v2(3);
G(3,3)=a(4)*v1(1)*v2(1)+a(4)*v1(2)*v2(2)+a(3)*v1(3)*v2(3);
G(2,1)=G(1,2); G(3,1)=G(1,3); G(3,2)=G(2,3);
end